# Arithmetic Operators in Python

# Addition
add_result = 10 + 5
print("Addition (10 + 5):", add_result)

# Subtraction
sub_result = 10 - 3
print("Subtraction (10 - 3):", sub_result)

# Multiplication
mul_result = 4 * 5
print("Multiplication (4 * 5):", mul_result)

# Division
div_result = 10 / 4
print("Division (10 / 4):", div_result)

# Floor Division
floor_result = 10 // 4
print("Floor Division (10 // 4):", floor_result)

# Modulus
mod_result = 10 % 4
print("Modulus (10 % 4):", mod_result)

# Exponential
exp_result = 2 ** 3
print("Exponential (2 ** 3):", exp_result)
